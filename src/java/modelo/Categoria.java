package modelo;

/**
 *
 * @author minay
 */
public class Categoria {
    int id;
    String Nombre;

    public Categoria() {
    }

    public Categoria(int id, String Nombre) {
        this.id = id;
        this.Nombre = Nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }
    
    
    
    
}
